#!/usr/bin/env node
// auto-populate-prompt.mjs — Generate an LLM prompt for brain auto-population
// This goes beyond bootstrap by preparing questions the LLM should answer about the codebase.
// Usage: node auto-populate-prompt.mjs <brain-dir> <project-dir>

import { readFileSync, writeFileSync, existsSync, mkdirSync, readdirSync, statSync } from "fs";
import { join, relative, extname, basename } from "path";
import { execSync } from "child_process";

const BRAIN_DIR = process.argv[2];
const PROJECT_DIR = process.argv[3];

if (!BRAIN_DIR || !PROJECT_DIR) {
  console.error("Usage: node auto-populate-prompt.mjs <brain-dir> <project-dir>");
  process.exit(1);
}

// Scan project structure (top 3 levels, skip noise)
function scanStructure(dir, depth = 0, maxDepth = 3) {
  if (depth > maxDepth) return [];
  const results = [];
  try {
    const entries = readdirSync(dir, { withFileTypes: true });
    for (const entry of entries) {
      if (entry.name.startsWith(".") && entry.name !== ".env.example") continue;
      if (["node_modules", "vendor", "dist", "build", "__pycache__", ".git", "coverage"].includes(entry.name)) continue;
      const full = join(dir, entry.name);
      const rel = relative(PROJECT_DIR, full);
      if (entry.isDirectory()) {
        results.push({ type: "dir", path: rel });
        results.push(...scanStructure(full, depth + 1, maxDepth));
      } else if (entry.isFile()) {
        try {
          const stat = statSync(full);
          if (stat.size < 500000) { // Skip files > 500KB
            results.push({ type: "file", path: rel, size: stat.size });
          }
        } catch {}
      }
    }
  } catch {}
  return results;
}

// Find key entry points
function findEntryPoints(structure) {
  const entryPatterns = [
    /^(src|lib|app)\/(index|main|app|server)\.(ts|js|py|go|rs)$/,
    /^(index|main|app|server)\.(ts|js|py|go|rs)$/,
    /^(src|lib)\/.*\.(ts|js|py|go|rs)$/,
  ];
  return structure
    .filter(s => s.type === "file" && entryPatterns.some(p => p.test(s.path)))
    .map(s => s.path)
    .slice(0, 20);
}

// Find config files
function findConfigFiles(structure) {
  const configPatterns = [
    /\.(json|yaml|yml|toml|ini|cfg|conf)$/,
    /^(Makefile|Dockerfile|docker-compose|Taskfile)/,
    /^\.env\.example$/,
  ];
  return structure
    .filter(s => s.type === "file" && configPatterns.some(p => p.test(s.path)))
    .map(s => s.path)
    .slice(0, 15);
}

// Count files by extension
function countByExtension(structure) {
  const counts = {};
  for (const s of structure.filter(s => s.type === "file")) {
    const ext = extname(s.path) || "(no ext)";
    counts[ext] = (counts[ext] || 0) + 1;
  }
  return Object.entries(counts).sort((a, b) => b[1] - a[1]).slice(0, 10);
}

// Read existing brain notes to avoid duplication
function readExistingBrainNotes(brainDir) {
  const indexPath = join(brainDir, "brain-index.json");
  if (!existsSync(indexPath)) return [];
  try {
    const index = JSON.parse(readFileSync(indexPath, "utf8"));
    return (index.entries || []).map(e => e.p);
  } catch {
    return [];
  }
}

// Get git info
function git(cmd) {
  try {
    return execSync(`git -C "${PROJECT_DIR}" ${cmd}`, { encoding: "utf-8", timeout: 10000, stdio: ["pipe", "pipe", "pipe"] }).trim();
  } catch { return null; }
}

// Main
const structure = scanStructure(PROJECT_DIR);
const entryPoints = findEntryPoints(structure);
const configFiles = findConfigFiles(structure);
const extCounts = countByExtension(structure);
const existingNotes = readExistingBrainNotes(BRAIN_DIR);
const brainFolderName = basename(BRAIN_DIR);

const recentCommits = git("log --oneline -10 --format='%s'")?.split("\n").filter(Boolean) || [];
const branches = git("branch --format='%(refname:short)'")?.split("\n").filter(Boolean) || [];

const prompt = `# Auto-Populate Brain: ${basename(PROJECT_DIR)}

This is an auto-populate prompt generated by the Second Brain framework.
When you see this file, please analyze this project and create brain notes.

## Project Structure Summary

**Files by type:**
${extCounts.map(([ext, count]) => `- ${ext}: ${count} files`).join("\n")}

**Entry points:**
${entryPoints.map(p => `- \`${p}\``).join("\n") || "- None detected"}

**Config files:**
${configFiles.map(p => `- \`${p}\``).join("\n") || "- None detected"}

**Recent commits:**
${recentCommits.map(c => `- ${c}`).join("\n") || "- No commits"}

**Branches:**
${branches.map(b => `- \`${b}\``).join("\n") || "- No branches"}

## Existing Brain Notes (skip these topics)

${existingNotes.map(p => `- ${p}`).join("\n") || "- None yet"}

## Instructions

Please analyze this project and create the following brain notes in \`${brainFolderName}/\`:

### 1. Architecture Overview (if not already present)
Read the key entry points and create a \`docs/architecture-overview.md\` note describing:
- Overall architecture pattern (monolith, microservices, serverless, etc.)
- Key modules/packages and their responsibilities
- Data flow between components
- External integrations

### 2. Key Decisions (if discoverable)
Look at recent commits, config choices, and code patterns. Create \`knowledge/graph/research/\` notes for:
- Major technology choices and why they were likely made
- Architectural patterns used

### 3. Development Workflow
Create a \`knowledge/memory/\` note documenting:
- How to run the project locally
- How to run tests
- CI/CD patterns detected
- Environment setup requirements

### 4. Active Work Threads
Based on recent commits and branches, update \`00-home/top-of-mind.md\` with:
- What seems to be actively worked on
- Open feature branches and their likely purpose

### 5. Project Mission (if not already set)
Based on README, package.json, or code analysis, create or update \`goal/mission.md\`.

## Rules

- Use templates from \`${brainFolderName}/_assets/templates/\`
- Always include YAML frontmatter
- One atomic claim per knowledge note
- Use prose-as-title naming for claims
- Run \`${brainFolderName}/hooks/rebuild-brain-index.sh\` after creating notes
`;

// Write the prompt
const stateDir = join(BRAIN_DIR, "hooks", ".state");
if (!existsSync(stateDir)) mkdirSync(stateDir, { recursive: true });
const promptPath = join(stateDir, "auto-populate-prompt.md");
writeFileSync(promptPath, prompt, "utf8");

console.log(`  Auto-populate prompt written to: ${promptPath}`);
console.log(`  Project: ${structure.filter(s => s.type === "file").length} files, ${structure.filter(s => s.type === "dir").length} dirs`);
console.log(`  Entry points: ${entryPoints.length}, Config files: ${configFiles.length}`);
