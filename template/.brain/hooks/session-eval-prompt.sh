#!/bin/bash
# Second Brain Framework — Session Evaluation & Auto-Persist
# Runs on Stop event. Does TWO things:
# 1. Auto-creates a minimal session log (guaranteed persistence)
# 2. Prints health metrics + checklist for the agent to act on
BRAIN_DIR="$(cd "$(dirname "$0")/.." && pwd)"
BRAIN_FOLDER_NAME="$(basename "$BRAIN_DIR")"
TODAY=$(date +%Y-%m-%d)
TIMESTAMP=$(date +%H%M)

# ── Collect Metrics ──────────────────────────────────────────────────

NOTE_COUNT=$(find "$BRAIN_DIR" -name "*.md" -not -path "*/_assets/templates/*" -not -path "*/.obsidian/*" -not -path "*/hooks/*" 2>/dev/null | wc -l | tr -d ' ')

ORPHANS=$(find "$BRAIN_DIR/knowledge" "$BRAIN_DIR/sessions" -name "*.md" 2>/dev/null | while read f; do
  links=$(grep -c '\[\[' "$f" 2>/dev/null || echo "0")
  links=$(echo "$links" | tr -d ' ')
  name=$(basename "$f" .md)
  backlinks=$(grep -rl "\[\[$name\]\]" "$BRAIN_DIR" 2>/dev/null | wc -l | tr -d ' ')
  backlinks=$(echo "$backlinks" | tr -d ' ')
  if [ "${links:-0}" -eq 0 ] && [ "${backlinks:-0}" -eq 0 ]; then
    echo "$f"
  fi
done | wc -l | tr -d ' ')

MISSING_KEYWORDS=$(find "$BRAIN_DIR/knowledge" "$BRAIN_DIR/sessions" -name "*.md" 2>/dev/null | while read f; do
  if ! grep -q "^keywords:" "$f" 2>/dev/null; then
    echo "$f"
  fi
done | wc -l | tr -d ' ')

# Check if top-of-mind was modified today
TOM="$BRAIN_DIR/00-home/top-of-mind.md"
TOM_STALE="no"
if [ -f "$TOM" ]; then
  TOM_MOD=$(stat -f %Sm -t %Y-%m-%d "$TOM" 2>/dev/null || stat -c %y "$TOM" 2>/dev/null | cut -d' ' -f1)
  if [ "$TOM_MOD" != "$TODAY" ]; then
    TOM_STALE="yes"
  fi
fi

# Check goal status
GOAL_EXISTS="no"
if [ -f "$BRAIN_DIR/goal/mission.md" ]; then
  MISSION_EMPTY=$(grep -c "^-$\|_Define\|_One sentence" "$BRAIN_DIR/goal/mission.md" 2>/dev/null || echo 0)
  if [ "$MISSION_EMPTY" -gt 2 ]; then
    GOAL_EXISTS="unfilled"
  else
    GOAL_EXISTS="yes"
  fi
fi

# Count files modified during this session (last 2 hours)
FILES_MODIFIED=$(find "$BRAIN_DIR" -name "*.md" -mmin -120 -not -path "*/_assets/templates/*" -not -path "*/.obsidian/*" -not -path "*/hooks/*" 2>/dev/null | wc -l | tr -d ' ')

# List files modified during this session
MODIFIED_LIST=$(find "$BRAIN_DIR" -name "*.md" -mmin -120 -not -path "*/_assets/templates/*" -not -path "*/.obsidian/*" -not -path "*/hooks/*" 2>/dev/null | while read f; do
  echo "  - $(basename "$f" .md)"
done)

# ── Auto-Create Session Log ─────────────────────────────────────────
# This guarantees every session leaves a trace, even if the agent
# doesn't explicitly persist.

SESSION_DIR="$BRAIN_DIR/sessions"
mkdir -p "$SESSION_DIR"
SESSION_FILE="$SESSION_DIR/${TODAY}-${TIMESTAMP}.md"

# Only create if no session log exists for today with this timestamp
if [ ! -f "$SESSION_FILE" ]; then
  cat > "$SESSION_FILE" << SESSIONEOF
---
date: "${TODAY}"
type: session
project: ""
agent: "claude"
tags:
  - session
  - auto-generated
keywords: []
---

# Session Log — ${TODAY} ${TIMESTAMP}

_Auto-generated by session-eval hook. Expand with details if significant._

## Brain Health at Session End

| Metric | Value |
|--------|-------|
| Total notes | ${NOTE_COUNT} |
| Orphan notes | ${ORPHANS} |
| Missing keywords | ${MISSING_KEYWORDS} |
| Top-of-mind stale | ${TOM_STALE} |
| Goal defined | ${GOAL_EXISTS} |
| Files modified this session | ${FILES_MODIFIED} |

## Files Modified

${MODIFIED_LIST:-_No brain files modified this session._}

## What Happened

_TODO: Fill in what was accomplished this session._

## What Was Learned

_TODO: Fill in key insights that should become knowledge claims._

## Router Misses

_List any queries where the brain-router returned irrelevant results or found nothing:_
- Query: "" → Expected: "" → Got: ""

_Keywords to add to existing notes based on misses:_
-

## Persisted To Memory

- [ ] top-of-mind.md updated
- [ ] Knowledge claims created for new insights
- [ ] Router misses addressed (keywords added to notes)
- [ ] Auto-memory updated if project context changed
SESSIONEOF
fi

# ── Write .last-session-notes for cross-session surfacing ────────────
LAST_SESSION_FILE="$BRAIN_DIR/00-home/.last-session-notes"
RECENT_KNOWLEDGE=$(find "$BRAIN_DIR/knowledge" "$BRAIN_DIR/docs" "$BRAIN_DIR/sessions" \
  -name "*.md" -mmin -120 -type f 2>/dev/null | while read f; do
  relpath=$(echo "$f" | sed "s|$BRAIN_DIR/||")
  name=$(basename "$f" .md)
  echo "- [[$name]] → $relpath"
done)

if [ -n "$RECENT_KNOWLEDGE" ]; then
  cat > "$LAST_SESSION_FILE" << LASTEOF
# Last Session Notes (auto-generated)
_Created: ${TODAY} ${TIMESTAMP}. Read these during Orient._

${RECENT_KNOWLEDGE}
LASTEOF
fi

# ── Rebuild Brain Index ──────────────────────────────────────────────
if command -v node &>/dev/null; then
  "$BRAIN_DIR/hooks/rebuild-brain-index.sh" "$BRAIN_DIR" 2>/dev/null &
fi

# ── Print Health Check ───────────────────────────────────────────────
cat << EVALEOF

SESSION END — BRAIN HEALTH CHECK
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
Total notes:           ${NOTE_COUNT}
Files modified:        ${FILES_MODIFIED}
Orphan notes:          ${ORPHANS} (target: 0)
Missing keywords:      ${MISSING_KEYWORDS} (target: 0)
Top-of-mind stale:     ${TOM_STALE}
Goal defined:          ${GOAL_EXISTS}

AUTO-PERSISTED:
  Session log: ${BRAIN_FOLDER_NAME}/sessions/${TODAY}-${TIMESTAMP}.md
  Last-session notes: ${BRAIN_FOLDER_NAME}/00-home/.last-session-notes

STOP — PERSIST STEP REQUIRED
━━━━━━━━━━━━━━━━━━━━━━━━━━━━
You CANNOT end this session without completing these actions:

1. Edit ${BRAIN_FOLDER_NAME}/sessions/${TODAY}-${TIMESTAMP}.md:
   - Replace "TODO" in "What Happened" with a 2-3 sentence summary
   - Replace "TODO" in "What Was Learned" with key insights
   - Fill in "Router Misses" if the brain missed relevant notes

2. Edit ${BRAIN_FOLDER_NAME}/00-home/top-of-mind.md:
   - Update current phase if it changed
   - Update active projects, open questions, blockers

A session log with empty TODOs means the brain learned NOTHING.
Do this NOW before you respond with anything else.

EVALEOF
